a = "my name is emmanuella esezobor, i love codes."
# 1. str.capitalize(): return a copy of the sting with the first character capitalised and the rest lowercased.
print(str.capitalize(a))
# 2. str.title() or : makes all words start with a capital letter
print("my name is emmanuella esezobor, i love codes.".title()) # or
print(str.title(a))
# 3. str.swapcase(): makes all uppercase convert to lowercased and vice versa
print(str.swapcase(a))
